package exercise;

/**
 * ClassName: Float
 * Description: calculate the area of rounds
 *
 * @author Ignorant
 * @create 2023/9/29 15:09
 */
public class Float {
    public static void main(String[] args) {
        double pi = 3.14;

        double r1 = 1.2;
        double r2 = 2.5;
        double r3 = 6;

        double s1 = pi*r1*r1;
        double s2 = pi*r2*r2;
        double s3 = pi*r3*r3;

        System.out.println(s1);
        System.out.println(s2);
        System.out.println(s3);
    }
}
